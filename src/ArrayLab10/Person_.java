package ArrayLab10;

public class Person_ {

	private String name;
	private String phone;

	public Person_(String n, String p) {
		name = n;
		phone = p;
	}

	public String getName() {
		return name;
	}

	public String getPhone() {
		return phone;
	}

	public void setName(String n) {
		this.name = n;
	}

	public void setPhone(String p) {
		this.phone = p;
	}
}
